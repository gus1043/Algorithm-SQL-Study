SELECT USED_GOODS_BOARD.TITLE, USED_GOODS_BOARD.BOARD_ID, USED_GOODS_REPLY.REPLY_ID, USED_GOODS_REPLY.WRITER_ID, USED_GOODS_REPLY.CONTENTS, TO_CHAR(USED_GOODS_REPLY.CREATED_DATE, 'YYYY-MM-DD') AS CREATED_DATE
FROM USED_GOODS_REPLY JOIN USED_GOODS_BOARD 
ON USED_GOODS_REPLY.BOARD_ID=USED_GOODS_BOARD.BOARD_ID
WHERE TO_CHAR(USED_GOODS_BOARD.CREATED_DATE, 'YYYY-MM-DD')>'2022-09-30'
AND TO_CHAR(USED_GOODS_BOARD.CREATED_DATE, 'YYYY-MM-DD')<'2022-11-01'
ORDER BY USED_GOODS_REPLY.CREATED_DATE ASC, TITLE ASC;